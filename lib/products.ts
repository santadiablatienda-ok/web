export interface Product {
  id: string
  name: string
  description: string
  specs?: string[]     // detalles tecnicos cortos, uno por linea (material, suela, cierre, etc.)
  price: number
  cost?: number       // costo de compra al proveedor (ARS), solo visible en el admin
  category: string
  brand?: string      // marca del producto (solo relevante en categoria "importados"), para el sub-filtro
  image: string
  imageAlt: string
  gallery?: string[]
  video?: string
  badge?: string
  featured?: boolean
  sizes?: string[]
  stock?: number        // -1 = encargo puro, 0 = sin stock, >0 = con stock (fallback cuando no hay stock por talle)
  sizeStock?: Record<string, number>  // stock puntual por talle; si falta un talle acá se toma como 0
  isEncargo?: boolean
  active?: boolean      // false = desactivado a mano: se muestra "Agotado" en la tienda pero no se puede comprar ni encargar (default true)
  colors?: string[]
  discountPercent?: number  // 0-100, descuento simple sobre el precio
  season?: string           // etiqueta libre, ej: "Verano 2026", "Invierno 2026"
}

export function finalPrice(product: Pick<Product, "price" | "discountPercent">): number {
  if (!product.discountPercent || product.discountPercent <= 0) return product.price
  return Math.round(product.price * (1 - product.discountPercent / 100))
}

